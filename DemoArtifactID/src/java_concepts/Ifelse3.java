package java_concepts;

public class Ifelse3 {

	public static void main(String[] args) {
		
		int q = 500; 
		int w = 400; 
		
		if(q < w) {
			System.out.println("q is lesser");
		}
		
		else if(q == w) {
			System.out.println("q is equal to w");
		}
		
		else {
			System.out.println("q is greater than w");
		}

	}

}
